    개인정보의 파기절차 및 방법<br />
    <br />
    당사는 원칙적으로 개인정보 수집 및 이용목적이 달성되거나, 보유 및 이용기간이 경과된 후에는 해당 정보를 지체 없이 파기합니다.<br />
    파기절차 및 방법은 다음과 같습니다.<br />
    <br />
    1) 파기절차<br />
    – 회원님이 회원가입 등을 위해 입력하신 정보는 목적이 달성된 후 내부 방침 및 기타 관련 법령에 의한 정보보호 사유에 따라(보유 및 이용기간 참조) 일정 기간 저장된 후 파기되어집니다. 동 개인정보는 법률에 의한 경우가 아니고서는 보유되어지는 이외의 다른 목적으로 이용되지 않습니다.<br />
    <br />
    2) 파기방법<br />
    – 종이에 출력된 개인정보는 분쇄기로 분쇄하거나 소각을 통하여 파기하고<br />
    – 전자적 파일형태로 저장된 개인정보는 기록을 재생할 수 없는 기술적 방법을 사용하여 삭제합니다.<br />

    <br />
    1) 서비스 제공에 관한 계약 이행 및 서비스 제공에 따른 요금정산<br />
    – 콘텐츠 제공, 물품배송 또는 청구서 등 발송, 금융거래 본인 인증 및 금융 서비스, 구매 및 요금 결제, 요금추심<br />
    <br />
    2) 회원 관리<br />
    – 회원제 서비스 이용에 따른 본인확인, 개인식별, 불량회원의 부정 이용 방지와 비인가 사용 방지, 가입 의사 확인, 가입 및 가입횟수 제한, 만14세 미만 아동 개인 정보 수집 시 법정 대리인 동의 여부 확인, 추후 법정 대리인 본인확인, 분쟁 조정을 위한 기록보존, 불만처리 등 민원처리,고지사항 전달<br />
    <br />
    개인정보의 제공 및 공유<br />
    <br />
    원칙적으로 당사는 회원님의 개인정보를 수집 및 이용목적에 한해서만 이용하며 타인 또는 타기업 기관에 공개하지 않습니다. 다만, 아래의 경우에는 예외로 합니다.<br />
